// Definimos la clase Modulo
class Modulo {
    int num;  // Variable para almacenar el primer número
    int num2; // Segunda variable para almacenar el divisor

    // Constructor de la clase
    Modulo() {}

    // Método para obtener el valor de num
    public int getNum() {
        return this.num;
    }

    // Método para asignar un valor a num
    public void setNum(int num) {
        this.num = num;
    }

    // Método para obtener el valor de num2
    public int getNum2() {
        return this.num2;
    }

    // Método para asignar un valor a num2
    public void setNum2(int num2) {
        this.num2 = num2;
    }
}
